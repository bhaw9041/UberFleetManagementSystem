    private void fetchCarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_fetchCarMouseClicked
        // TODO add your handling code here:
        Car c = this.uberFleet.getCarBySerialNumber(this.serialNumber.getText());
        if(c != null){
            DateFormat dateFormat = new SimpleDateFormat("mm/dd/yyyy");  
            this.maintenanceExpiryDate.setText(dateFormat.format(c.getMaintenaceExpiry()));
            this.manufacturedYearTxt.setText(String.valueOf(c.getManufacturedYear()));
            this.manufacturerTxt.setText(c.getManufacturer());
            this.modelNumberTxt.setText(c.getModelNumber());
            this.operatingCity.setText(c.getGeoLocality());
            this.seatingCapacity.setText(String.valueOf(c.getSeats()));
            this.isAvailable.setSelected(c.isAvailable());
            this.showAllFields();
        }else{
            this.hideAllFields();
            JOptionPane.showMessageDialog(this, "No Car for Searched Serial Number");
        }
    }//GEN-LAST:event_fetchCarMouseClicked

