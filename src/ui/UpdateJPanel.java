    private void saveToFleetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveToFleetMouseClicked
        // TODO add your handling code here:
        if(this.manufacturedYearTxt.getText().equals("")  || this.manufacturerTxt.getText().equals("")
                || this.seatingCapacity.getText().equals("")|| this.modelNumberTxt.getText().equals("") 
                || this.operatingCity.getText().equals("")
                || this.maintenanceExpiryDate.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Missing Field");
             return;
        }
        try {

        boolean available = this.isAvailable.isSelected();
        String manufacturer = this.manufacturerTxt.getText();
        int manufacturedYear = Integer.parseInt(this.manufacturedYearTxt.getText());
        int seats = Integer.parseInt(this.seatingCapacity.getText());
        String serialNumber = this.serialNumber.getText();
        String modelNumber = this.modelNumberTxt.getText();
        String geoLocality = this.operatingCity.getText();
        Date maintenaceExpiry = new SimpleDateFormat("mm/dd/yyyy").parse(this.maintenanceExpiryDate.getText());
        Car c = new Car(available, manufacturer, manufacturedYear, seats, serialNumber,modelNumber, geoLocality,maintenaceExpiry);
        this.uberFleet.updateCar(c, serialNumber);
        JOptionPane.showMessageDialog(this, "Car Updated In Uber Fleet");
         
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Invalid Field");
        }
        
    }//GEN-LAST:event_saveToFleetMouseClicked

    
